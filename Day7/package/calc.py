class op:
 def add(self, num1,num2): 
    return num1+num2

 def multiply(self, num1,num2):
    return num1*num2

 def avg(self, num1,num2): 
    return (num1+num2)/2

 def sub(self, num1,num2):
    return num1-num2

 def div(self, num1,num2):
    return num1/num2